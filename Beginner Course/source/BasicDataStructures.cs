using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Beginner_Course.source
{
    public class BasicDataStructures
    {
        public static void Run()
        {
            string word = "this is a string";
            char letter = 'A';
            Int32 number = 123;
            float float_number = 123.1231223f;
            double decimal_number = 123.333;
            bool boolean = true;
            Console.WriteLine(word);
            Console.WriteLine("this is a character: " + letter);
            Console.WriteLine("this is a number: " + number);
            Console.WriteLine("this is a float: " + float_number);
            Console.WriteLine("this is a double precision number: " + decimal_number);
            Console.WriteLine("this is a boolean value: " +  boolean);
            Console.ReadLine();
        }
    }
}
